CREATE TABLE tenants (
  tenant_id STRING,
  full_name STRING,
  first_name STRING,
  last_name STRING,
  phone STRING,
  alternate_phone STRING,
  email STRING,
  date_of_birth DATE,
  occupation STRING,
  employer_name STRING,
  employer_phone STRING,
  property_address STRING,
  property_type STRING,
  property_id STRING,
  owner_name STRING,
  owner_phone STRING,
  owner_email STRING,
  monthly_rent DECIMAL(10,2),
  maintenance_charges DECIMAL(6,2),
  lease_start_date DATE,
  lease_end_date DATE,
  security_deposit DECIMAL(12,2),
  notice_period_days INT,
  tenants_count INT,
  pets_allowed BOOLEAN,
  vehicle_type STRING,
  parking_slot STRING,
  status STRING
);

CREATE TABLE properties (
  property_id STRING,
  property_name STRING,
  property_type STRING,
  address STRING,
  locality STRING,
  city STRING,
  state STRING,
  pincode STRING,
  floor_no INT,
  total_floors INT,
  bedrooms INT,
  bathrooms INT,
  carpet_area_sqft INT,
  built_up_area_sqft INT,
  furnished_status STRING,
  rent DECIMAL(10,2),
  security_deposit DECIMAL(12,2),
  availability_date DATE,
  owner_name STRING,
  owner_phone STRING,
  owner_email STRING,
  owner_pan STRING,
  tenant_id STRING,
  status STRING,
  brokerage DECIMAL(8,2),
  maintenance_included BOOLEAN
);

CREATE TABLE payments (
  payment_id STRING,
  tenant_id STRING,
  property_id STRING,
  payment_type STRING,
  amount DECIMAL(10,2),
  payment_date DATE,
  due_date DATE,
  payment_mode STRING,
  reference_no STRING,
  cheque_no STRING,
  bank_name STRING,
  late_fee DECIMAL(6,2),
  total_amount DECIMAL(10,2),
  month INT,
  year INT,
  recorded_by STRING,
  status STRING
);

CREATE TABLE maintenance (
  complaint_id STRING,
  tenant_id STRING,
  property_id STRING,
  complaint_type STRING,
  description TEXT,
  priority STRING,
  assigned_to STRING,
  scheduled_date DATE,
  resolved_date DATE,
  resolution_notes TEXT,
  cost DECIMAL(8,2),
  status STRING,
  created_at TIMESTAMP
);
