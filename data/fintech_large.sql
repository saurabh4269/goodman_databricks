CREATE TABLE customers (
  customer_id STRING,
  full_name STRING,
  first_name STRING,
  last_name STRING,
  phone_no STRING,
  alternate_phone STRING,
  email STRING,
  alternate_email STRING,
  aadhaar_no STRING,
  pan_no STRING,
  voter_id STRING,
  passport_no STRING,
  dl_no STRING,
  mother_name STRING,
  father_name STRING,
  spouse_name STRING,
  guardian_name STRING,
  date_of_birth DATE,
  gender STRING,
  marital_status STRING,
  occupation STRING,
  annual_income DECIMAL(12,2),
  account_no STRING,
  ifsc_code STRING,
  bank_name STRING,
  branch_name STRING,
  upi_id STRING,
  kyc_status STRING,
  kyc_verified_by STRING,
  kyc_verified_at TIMESTAMP,
  risk_score INT,
  risk_category STRING,
  referral_code STRING,
  source_channel STRING
);

CREATE TABLE transactions (
  txn_id STRING,
  customer_id STRING,
  amount DECIMAL(14,2),
  currency STRING,
  txn_type STRING,
  txn_mode STRING,
  upi_handle STRING,
  merchant_id STRING,
  merchant_name STRING,
  merchant_category STRING,
  merchant_city STRING,
  utr_no STRING,
  ref_id STRING,
  narration STRING,
  remarks TEXT,
  status STRING,
  failure_reason STRING,
  created_at TIMESTAMP,
  completed_at TIMESTAMP,
  settlement_date DATE
);

CREATE TABLE cards (
  card_id STRING,
  customer_id STRING,
  card_last4 STRING,
  cvv_hash STRING,
  card_type STRING,
  card_brand STRING,
  expiry_month INT,
  expiry_year INT,
  card_issuer STRING,
  card_network STRING,
  card_status STRING,
  credit_limit DECIMAL(12,2),
  available_credit DECIMAL(12,2),
  issued_at TIMESTAMP,
  activated_at TIMESTAMP,
  blocked_at TIMESTAMP
);

CREATE TABLE addresses (
  address_id STRING,
  customer_id STRING,
  address_type STRING,
  care_of STRING,
  street_address STRING,
  locality STRING,
  city STRING,
  district STRING,
  state STRING,
  pincode STRING,
  landmark STRING,
  latitude DECIMAL(10,7),
  longitude DECIMAL(10,7),
  is_verified BOOLEAN,
  verified_by STRING,
  verified_at TIMESTAMP
);

CREATE TABLE loan_applications (
  loan_id STRING,
  customer_id STRING,
  loan_type STRING,
  principal_amount DECIMAL(14,2),
  tenure_months INT,
  interest_rate DECIMAL(5,2),
  emi_amount DECIMAL(12,2),
  purpose STRING,
  employer_name STRING,
  monthly_salary DECIMAL(12,2),
  status STRING,
  approved_by STRING,
  disbursed_at TIMESTAMP,
  first_emi_date DATE
);

CREATE TABLE repayments (
  repayment_id STRING,
  loan_id STRING,
  customer_id STRING,
  emi_no INT,
  amount DECIMAL(12,2),
  principal_portion DECIMAL(12,2),
  interest_portion DECIMAL(12,2),
  balance_outstanding DECIMAL(14,2),
  due_date DATE,
  paid_date DATE,
  payment_mode STRING,
  utr_no STRING,
  status STRING
);
