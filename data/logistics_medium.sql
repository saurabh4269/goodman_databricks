CREATE TABLE shipments (
  shipment_id STRING,
  tracking_no STRING,
  shipper_name STRING,
  shipper_phone STRING,
  shipper_address STRING,
  shipper_city STRING,
  shipper_gst_no STRING,
  receiver_name STRING,
  receiver_phone STRING,
  receiver_address STRING,
  receiver_city STRING,
  receiver_pincode STRING,
  origin_city STRING,
  destination_city STRING,
  origin_state STRING,
  destination_state STRING,
  weight_kg DECIMAL(8,2),
  dimensions_cm STRING,
  chargeable_weight DECIMAL(8,2),
  shipment_type STRING,
  service_type STRING,
  declared_value DECIMAL(12,2),
  cod_amount DECIMAL(10,2),
  freight_charge DECIMAL(8,2),
  insurance_amount DECIMAL(6,2),
  gst_percent INT,
  total_charge DECIMAL(12,2),
  payment_mode STRING,
  pickup_date DATE,
  expected_delivery_date DATE,
  actual_delivery_date DATE,
  status STRING
);

CREATE TABLE deliveries (
  delivery_id STRING,
  shipment_id STRING,
  tracking_no STRING,
  driver_name STRING,
  driver_phone STRING,
  vehicle_no STRING,
  vehicle_type STRING,
  delivery_address STRING,
  delivery_city STRING,
  delivery_pincode STRING,
  delivery_date DATE,
  delivery_time STRING,
  recipient_name STRING,
  recipient_phone STRING,
  recipient_relationship STRING,
  otp_verified BOOLEAN,
  delivery_status STRING,
  attempt_count INT,
  failure_reason TEXT,
  proof_of_delivery_url STRING,
  signature_url STRING
);

CREATE TABLE vehicles (
  vehicle_id STRING,
  vehicle_no STRING,
  vehicle_type STRING,
  model STRING,
  capacity_kg DECIMAL(8,2),
  cubic_capacity STRING,
  owner_name STRING,
  owner_phone STRING,
  rc_no STRING,
  rc_expiry DATE,
  insurance_no STRING,
  insurance_expiry DATE,
  pollution_cert_expiry DATE,
  fitness_cert_expiry DATE,
  driver_name STRING,
  driver_phone STRING,
  driver_license_no STRING,
  driver_license_expiry DATE,
  status STRING
);
