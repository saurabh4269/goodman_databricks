CREATE TABLE policyholders (
  holder_id STRING,
  full_name STRING,
  first_name STRING,
  last_name STRING,
  phone STRING,
  alternate_phone STRING,
  email STRING,
  date_of_birth DATE,
  age INT,
  gender STRING,
  occupation STRING,
  employer_name STRING,
  annual_income DECIMAL(12,2),
  nominee_name STRING,
  nominee_relationship STRING,
  nominee_dob DATE,
  guardian_name STRING,
  guardian_relationship STRING,
  pan_no STRING,
  aadhaar_no STRING,
  voter_id STRING,
  passport_no STRING,
  address STRING,
  city STRING,
  state STRING,
  pincode STRING,
  policy_no STRING,
  policy_type STRING,
  product_name STRING,
  sum_insured DECIMAL(14,2),
  premium DECIMAL(10,2),
  premium_frequency STRING,
  policy_start_date DATE,
  policy_end_date DATE,
  last_premium_date DATE,
  next_premium_date DATE,
  lapsed BOOLEAN,
  status STRING
);

CREATE TABLE policies (
  policy_id STRING,
  policy_no STRING,
  holder_id STRING,
  product_name STRING,
  plan_name STRING,
  sum_insured DECIMAL(14,2),
  tenure_years INT,
  premium DECIMAL(10,2),
  premium_frequency STRING,
  coverage_details TEXT,
  exclusions TEXT,
  waiting_period_months INT,
  policy_start_date DATE,
  policy_end_date DATE,
  status STRING,
  free_look_period_end DATE,
  nomination_registered BOOLEAN
);

CREATE TABLE claims (
  claim_id STRING,
  holder_id STRING,
  policy_no STRING,
  claim_type STRING,
  claim_subtype STRING,
  claim_amount DECIMAL(12,2),
  approved_amount DECIMAL(12,2),
  rejected_amount DECIMAL(12,2),
  deduction_reason TEXT,
  hospital_name STRING,
  hospital_city STRING,
  diagnosis TEXT,
  icd_code STRING,
  admission_date DATE,
  discharge_date DATE,
  days_hospitalized INT,
  treating_doctor_name STRING,
  claim_handler_name STRING,
  status STRING,
  submitted_at TIMESTAMP,
  reviewed_at TIMESTAMP,
  approval_date DATE,
  rejection_date DATE,
  rejection_reason TEXT,
  settlement_date DATE,
  utr_no STRING
);

CREATE TABLE beneficiaries (
  beneficiary_id STRING,
  holder_id STRING,
  beneficiary_name STRING,
  relationship STRING,
  share_percent INT,
  date_of_birth DATE,
  phone STRING,
  email STRING,
  bank_ac_no STRING,
  ifsc_code STRING,
  is_minor BOOLEAN,
  guardian_name STRING,
  is_primary BOOLEAN
);
